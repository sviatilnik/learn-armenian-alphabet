package ui

import "io"

// Title выводит приветствие, описание приложения и способ выхода.
func Title(w io.Writer) {
	w.Write([]byte("Добро пожаловать в тренажёр армянского алфавита!\n\n"))
	w.Write([]byte("Тренажёр помогает запомнить буквы и их названия/звуки. Выберите режим, отвечайте на вопросы. В любой момент выйти из игры можно по Ctrl+C.\n\n"))
}

// Menu выводит меню выбора режима.
func Menu(w io.Writer) {
	w.Write([]byte(`Выберите режим:
[1] Назвать букву (по символу)
[2] Назвать звук/транслитерацию (по символу)
[3] Показать алфавит
Выбор:`))
}

// InvalidMode выводит сообщение о неверном режиме и повторяет меню.
func InvalidMode(w io.Writer) {
	w.Write([]byte("Некорректный режим!\n"))
	Menu(w)
}

// End выводит прощание при завершении.
func End(w io.Writer) {
	w.Write([]byte("\nСпасибо за игру!\n"))
}
